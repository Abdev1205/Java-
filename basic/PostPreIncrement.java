public class PostPreIncrement {

  public static void main(String[] args) {
    // this is post increment  in which first it do the opertaion then inceremtes the value
    int a = 10;
    int b = a++;

    // this is pre increment  in which its incremetes first and then it do the opertaion
    // int a = 10;
    // int b = ++a;
    System.out.println(a);
    System.out.println(b);
  }
}
